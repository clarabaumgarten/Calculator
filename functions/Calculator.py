class Calculator:
    def subtraction(self, num_one, num_twoo):
        return num_one - num_twoo

    def addition(self, num_one, num_twoo):
        return num_one + num_twoo

    def multiplication(self, num_one, num_twoo):
        return num_one * num_twoo

    def division(self, num_one, num_twoo):
        return num_one / num_twoo
